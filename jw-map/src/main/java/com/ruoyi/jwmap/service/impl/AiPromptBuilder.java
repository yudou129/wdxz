    /**
     * ============ 功能7：四象限分析 ============
     */
    public String buildQuadrantAnalysisUserMessage(String dataContext) {
        return "请对以上四象限分布进行战略解读。\n\n" +
               "以下是四象限数据：\n\n" + dataContext + "\n\n" +
               "请按以下结构输出：\n\n" +
               "## 整体分布评价\n" +
               "1. 四个象限的分布比例是否健康？（Q1理想>25%，Q4理想<20%）\n" +
               "2. 是否存在系统性偏差？（如Q3+Q4占比过高说明选址与经营存在脱节）\n\n" +
               "## 各象限策略\n" +
               "- Q1（高能效+好地段）: 该象限网点如何巩固优势？\n" +
               "- Q2（高能效+差地段）: 位置差但经营好，是否可持续？需要关注什么？\n" +
               "- Q3（低能效+差地段）: 哪些是需要重点评估关停并转的？哪些还可以改善？\n" +
               "- Q4（低能效+好地段）: 这是最大的改善机会！列出Q4中效能最低的2-3个网点，分析可能原因\n\n" +
               "## 重点关注网点\n" +
               "1. 最需要立即行动的网点（Q4中效能最低的）\n" +
               "2. 最值得学习的标杆（Q1中效能最高的）\n" +
               "3. 最需评估存续的网点（Q3中双项垫底的）\n\n" +
               "## 改进预估\n" +
               "如果将Q4中效能最低的2个网点提升到效能中位数：\n" +
               "1. 城市整体平均分将提升约多少？\n" +
               "2. Q4网点占比将从X%降到Y%\n" +
               "3. 关键措施是什么？（管理层调整/人员补充/业态升级/面积优化）";

    }

    /**
     * ============ 功能8：迁址建议 ============
     */
    public String buildRelocationSuggestionUserMessage(String dataContext) {
        return "请对该网点进行迁址评估分析。\n\n" +
               "以下是该网点及其所在网格的完整数据：\n\n" + dataContext + "\n\n" +
               "请严格按以下结构输出，每个章节用 ## 标题：\n\n" +
               "## 现状诊断\n" +
               "1. 该网格的综合选址得分及三支柱得分（人口/商业/企业），在全市的排名位置\n" +
               "2. 该网点的效能综合得分及五维度得分，在全市的排名位置\n" +
               "3. 选址-效能匹配诊断：\n" +
               "   - 双高（Q1）→ 位置好经营好，不需迁址，建议深耕\n" +
               "   - 选址高+效能低（Q4）→ 有迁址价值，位置有潜力但经营未释放\n" +
               "   - 效能高+选址低（Q2）→ 可选迁址，团队能力强但选址受限，迁到好位置可释放更大潜力\n" +
               "   - 双低（Q3）→ 建议评估迁址或关停\n\n" +
               "## 网格适配度分析\n" +
               "1. 该网格的人口/商业/企业特征与网点当前业务侧重是否匹配\n" +
               "2. 如果该区域以零售客户为主但该网点偏对公，说明不匹配\n" +
               "3. 现有网点面积、人员配置与网格客群规模的匹配程度\n\n" +
               "## 网格内同业与POI环境\n" +
               "1. 网格内同业网点情况（名称列表）\n" +
               "2. 竞争密度评估\n" +
               "3. POI 设施特征（主要类型和数量）\n" +
               "4. 商业氛围判断\n\n" +
               "## 迁址建议结论\n" +
               "1. 建议：推荐迁址 / 不推荐迁址 / 可选迁址\n" +
               "2. 核心理由（一句话）\n" +
               "3. 如推荐迁址：\n" +
               "   - 推荐迁往区域类型描述\n" +
               "   - 距原址参考距离\n" +
               "   - 预估迁址后可带来的变化（基于与相邻网格的指标对比，人口/商业等方面的差异）\n" +
               "   - 新网点建议配置（类型+人员+面积）\n" +
               "4. 如不推荐迁址：\n" +
               "   - 建议保留并优化的理由\n" +
               "   - 2-3 条具体效能提升措施\n\n" +
               "## 候选推荐网格（仅推荐迁址时展示）\n" +
               "列出适合作为迁址目标的候选网格（评分最高且距离合适的空白网格）：\n" +
               "1. 候选网格1：网格编码，选址得分，三支柱得分，距本网点距离，推荐理由\n" +
               "2. 候选网格2：同上信息\n" +
               "3. 候选网格3：同上信息\n\n" +
               "注意：以上所有分析仅基于提供的数据，建议结合实地考察、当地市场调研等信息综合决策。";
    }

